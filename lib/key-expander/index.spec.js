'use strict';

const expect = require('chai').expect;
const KeyExpander = require('.');

xdescribe('KeyExpander', () => {

  describe('#getRoundKey', () => {

    it('returns the correct round key', () => {
      let key = [
        0x2b, 0x7e, 0x15, 0x16,
        0x28, 0xae, 0xd2, 0xa6,
        0xab, 0xf7, 0x15, 0x88,
        0x09, 0xcf, 0x4f, 0x3c
      ];
      let expander = new KeyExpander(key);

      expect(expander.getRoundKey(1)).to.eq([
        [0xa0, 0xfa, 0xfe, 0x17],
        [0x88, 0x54, 0x2c, 0x1b],
        [0x23, 0xa3, 0x39, 0x39],
        [0x2a, 0x6c, 0x76, 0x05]
      ]);
    });

  });

});
